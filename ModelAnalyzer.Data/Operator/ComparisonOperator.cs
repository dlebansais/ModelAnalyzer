namespace ModelAnalyzer;

/// <summary>
/// Represents a comparison operator.
/// </summary>
internal enum ComparisonOperator
{
    /// <summary>
    /// The &gt; operator.
    /// </summary>
    GreaterThan,

    /// <summary>
    /// The &gt;= operator.
    /// </summary>
    GreaterThanOrEqual,

    /// <summary>
    /// The &lt; operator.
    /// </summary>
    LessThan,

    /// <summary>
    /// The &lt;= operator.
    /// </summary>
    LessThanOrEqual,
}
